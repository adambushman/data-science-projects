library("tidyverse")

cluster_details <- data.frame(
    cluster = as.character(c(9, 5, 10, 6, 4, 8, 1, 7, 3, 2)),
    label = c(
        "Interior Anchor", "Interior Connector", "Interior Engine", "Perimeter Anchor", "Perimeter Engine",
        "Perimeter Finisher", "Versatile Anchor", "Versatile Connector", "Versatile Engine", "Versatile Finisher"
    ),
    abbrev = c(
        "IA", "IC", "IE", "PA", "PE", "PF", "VA", "VC", "VE", "VF"
    )
)


full_df <-
    bind_rows(
        df1, df2, df3, df4, df5,
        df6, df7, df8, df9, df10
    ) |>
    inner_join(
        cluster_details
    ) |>
    rename(cluster_name = cluster)



full_df |>
    group_by(label, abbrev) |>
    count() |>
    ungroup() |>
    mutate(perc = n / sum(n)) |>
    arrange(label)


nba_adjusted_full <- nba_adjusted |>
    bind_cols(nba_ids) |>
    inner_join(
        full_df,
        by = join_by(profile_name == player)
    )


write.csv(nba_adjusted_full, "nba-player-clustered.csv", row.names = FALSE)
